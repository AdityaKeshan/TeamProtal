package com.example.teamprotal;

public class Imges {
    int image;
    int height;
    int width;

    public Imges(int image, int height, int width) {
        this.image = image;
        this.height = height;
        this.width = width;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }
}
